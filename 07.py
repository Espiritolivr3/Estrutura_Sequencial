"""
Faça um Programa que calcule a
área de um quadrado, em seguida mostre
o dobro desta área para o usuário
"""

lado_a = int(input('Digite o valor de um lado: '))
lado_b = int(input('Digite o valor do outro lado: '))

metro_quadrado = lado_a * lado_b

print(f'O quadrado tem {metro_quadrado}m2')